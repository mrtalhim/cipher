import unittest
from extended_vigenere_cipher import extended_vigenere_cipher

class TestExtendedVigenereCipher(unittest.TestCase):

    def test_encrypt_quickfox(self):
        self.assertEqual(extended_vigenere_cipher('cipher', b'The quick brown fox jumps over the lazy dog', mode='encrypt', cipher_split=False), b'\xc2\xb7\xc3\x91\xc3\x95\xc2\x88\xc3\x96\xc3\xa7\xc3\x8c\xc3\x8c\xc3\x9b\xc2\x88\xc3\x87\xc3\xa4\xc3\x92\xc3\xa0\xc3\x9e\xc2\x88\xc3\x8b\xc3\xa1\xc3\x9b\xc2\x89\xc3\x9a\xc3\x9d\xc3\x92\xc3\xa2\xc3\x96\xc2\x89\xc3\x9f\xc3\x9e\xc3\x8a\xc3\xa4\xc2\x83\xc3\x9d\xc3\x98\xc3\x8d\xc2\x85\xc3\x9e\xc3\x84\xc3\xa3\xc3\xa9\xc2\x88\xc3\x89\xc3\xa1\xc3\x8a')

    def test_decrypt_quickfox(self):
        self.assertEqual(extended_vigenere_cipher('cipher', b'\xc2\xb7\xc3\x91\xc3\x95\xc2\x88\xc3\x96\xc3\xa7\xc3\x8c\xc3\x8c\xc3\x9b\xc2\x88\xc3\x87\xc3\xa4\xc3\x92\xc3\xa0\xc3\x9e\xc2\x88\xc3\x8b\xc3\xa1\xc3\x9b\xc2\x89\xc3\x9a\xc3\x9d\xc3\x92\xc3\xa2\xc3\x96\xc2\x89\xc3\x9f\xc3\x9e\xc3\x8a\xc3\xa4\xc2\x83\xc3\x9d\xc3\x98\xc3\x8d\xc2\x85\xc3\x9e\xc3\x84\xc3\xa3\xc3\xa9\xc2\x88\xc3\x89\xc3\xa1\xc3\x8a', mode='decrypt', cipher_split=False), b'The quick brown fox jumps over the lazy dog')

if __name__ == '__main__':
    unittest.main()